def select_night_post(day_of_year: int = None) -> str:
    """日替わりで投稿を選択（重複しないようローテーション）"""
    if day_of_year is None:
        day_of_year = datetime.now().timetuple().tm_yday
    idx = day_of_year % len(NIGHT_POSTS)
    return NIGHT_POSTS[idx]


def main():
    parser = argparse.ArgumentParser(description="夜の豆知識・Tips投稿")
    parser.add_argument("--post", "-p", action="store_true")
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--list", "-l", action="store_true")
    parser.add_argument("--index", "-i", type=int, default=None)
    args = parser.parse_args()

    if args.list:
        print(f"=== 豆知識投稿: {len(NIGHT_POSTS)}本 ===")
        for i, p in enumerate(NIGHT_POSTS):
            tlen = _twitter_len(p)
            status = "OK" if tlen <= 280 else f"OVER({tlen})"
            print(f"  [{i:2d}] {status:>8} | {p[:60].replace(chr(10), ' ')}")
        return 0

    if args.index is not None:
        tweet = NIGHT_POSTS[args.index % len(NIGHT_POSTS)]
    else:
        tweet = select_night_post()

    print("=" * 60)
    print("🌙 夜の豆知識投稿")
    print("=" * 60)
    print(tweet)
    print("-" * 60)
    tlen = _twitter_len(tweet)
    print(f"📊 文字数: {tlen}/280 {'⚠️ OVER!' if tlen > 280 else 'OK'}")

    if args.post and not args.dry_run:
        if tlen > 280:
            print("❌ 文字数オーバー")
            return 1
        if not check_connection():
            print("❌ 接続失敗")
            return 1
        print("\n📤 投稿中...")
        if post_to_twitter(tweet):
            print("✅ 投稿完了!")
        else:
            print("❌ 投稿失敗")
            return 1
    else:
        print("\n💡 --post で投稿")

    return 0


if __name__ == "__main__":
    sys.exit(main())
