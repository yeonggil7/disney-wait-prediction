def _twitter_len(text):
    WEIGHT1_RANGES = [
        (0x0000, 0x10FF),
        (0x2000, 0x200D),
        (0x2010, 0x201F),
        (0x2032, 0x2037),
    ]
    count = 0
    for ch in text:
        cp = ord(ch)
        w = 2
        for lo, hi in WEIGHT1_RANGES:
            if lo <= cp <= hi:
                w = 1
                break
        count += w
    return count


